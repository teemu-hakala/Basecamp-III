#include <stdlib.h>
#include "ft_list.h"

t_list		*ft_create_elem(void *data)
{
	t_list		*result;

	result = (t_list *)malloc(sizeof(result));
	if (!result)
		return (result);
	result->data = data;
	result->next = NULL;
	return (result);
}
