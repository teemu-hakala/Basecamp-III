#include <unistd.h>

void	aff_z(char *str)
{
	while (str)
	{
		if (*str == 'z')
		{
			write(1, str, 1);
			write(1, "\n", 1);
			return ;
		}
		str++;
	}
	write(1, "z\n", 2);
}
