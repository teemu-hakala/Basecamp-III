char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	u;

	u = 0;
	while (u < n && *(src + u))
	{
		*(dest + u) = *(src + u);
		u++;
	}
	while (u < n)
		*(dest + u++) = '\0';
	return (dest);
}
