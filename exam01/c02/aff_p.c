#include <unistd.h>

void	aff_p(char *str)
{
	while (*str && *str != 'p')
		str++;
	if (*str == 'p')
	{
		write(1, str, 1);
		write(1, "\n", 1);
	}
	else
		write(1, "p\n", 2);
}
